// Operadores em JavaScript

// Operadores aritméticos
var soma = 5 + 3;  // soma: 8
var subtracao = 5 - 3;  // subtracao: 2
var multiplicacao = 5 * 3;  // multiplicacao: 15
var divisao = 5 / 3;  // divisao: 1.6666666666666667
var modulo = 5 % 3;  // modulo: 2

// Operadores de comparação
var maior = 5 > 3;  // maior: true
var menor = 5 < 3;  // menor: false
var igual = 5 == "5";  // igual: true
var identico = 5 === "5";  // identico: false

// Operadores lógicos
var e = true && false;  // e: false
var ou = true || false;  // ou: true
var nao = !true;  // nao: false
